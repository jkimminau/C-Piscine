char	*ft_strlowcase(char *str)
{
	char *tmp;

	tmp = str;
	while (*tmp)
	{
		if (*tmp >= 'A' && *tmp <= 'Z')
			*tmp = *tmp - 'A' + 'a';
		tmp = tmp + 1;
	}
	return (str);
}
