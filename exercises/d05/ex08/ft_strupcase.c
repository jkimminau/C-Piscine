char	*ft_strupcase(char *str)
{
	char *tmp;

	tmp = str;
	while (*tmp)
	{
		if (*tmp >= 'a' && *tmp <= 'z')
			*tmp = *tmp - 'a' + 'A';
		tmp = tmp + 1;
	}
	return (str);
}
