int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 1;
	while (*s1 == *s2 && i < n)
	{
		s1 = s1 + 1;
		s2 = s2 + 1;
		i++;
	}
	if (*s1 != *s2)
		return (*s1 - *s2);
	return (0);
}
