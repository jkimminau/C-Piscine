int		ft_atoi(char *str)
{
	int s;
	int n;

	s = 1;
	n = 0;
	while ((*str < 33 && *str > 0) || *str == '+')
		str++;
	if (*str == '-' && s > 0)
	{
		s = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10;
		n += (*str - '0');
		str++;
	}
	return (n * s);
}
