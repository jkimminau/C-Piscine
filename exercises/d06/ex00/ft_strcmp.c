int		ft_strcmp(char *s1, char *s2)
{
	while (*s1 == *s2 && *s1 != 0 && *s2 != 0)
	{
		s1 = s1 + 1;
		s2 = s2 + 1;
	}
	if (*s1 != *s2)
		return (*s1 - *s2);
	return (0);
}
