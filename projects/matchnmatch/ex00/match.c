int		match(char *s1, char *s2)
{
	if (*s2 == '*')
	{
		while (*s2 == '*')
			s2++;
		while (*s1 != *s2 && *s1)
			s1++;
	}
	if (!*s1 && !*s2)
		return (1);
	else if (*s1 == *s2)
		return (1 * match(s1 + 1, s2 + 1));
	else
		return (0);
}
